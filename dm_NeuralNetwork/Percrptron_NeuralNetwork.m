%----------------感知机算法做逻辑与的划分问题---------------
clc;clear;
p = [0 0 1 1; 0 1 0 1]   %输入值
t = [0 0 0 1]   %目标值
plotpv(p,t)%绘制输入向量和目标向量。如果T只含一元矢量，则目标为0的输入矢量
%在坐标图中用符号＂o＂表示： 目标为1的输入矢量在坐标图中用符号＂+＂表示
net = newp(minmax(p),1);%创建一个感知器网络
net.iw{1,1} = [-1.2 -0.6];%设定权值
net.b{1} = 1;%设定阈值
plotpc(net.iw{1,1},net.b{1})%生成的图形是还没有经过训练的分界线
%----下面进行训练----
net.trainParam.epochs = 20;
net=train(net,p,t);
plotpv(p,t);
plotpc(net.iw{1,1},net.b{1})%训练之后的分界线